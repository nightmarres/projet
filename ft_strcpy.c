char	*ft_strcpy(char *s1, const char *s2);

char	*ft_strcpy(char *s1, const char *s2)
{
	int		a;

	a = 0;
	while (s2[a] != '\0')
	{
		s1[a] = s2[a];
		a = a + 1;
	}
	if (s2[a] == '\0')
		s1[a] = s2[a];
	return (s1);
}
